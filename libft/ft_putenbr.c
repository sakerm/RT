#include "libft.h"

void	ft_putenbr(int n)
{
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n >= 10)
	{
		ft_putenbr(n / 10);
		ft_putenbr(n % 10);
	}
	else
		ft_putchar(n + 48);
	ft_putchar('\n');
}
