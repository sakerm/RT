#include "libft.h"

char	*ft_strmap(char const *s, char (*f) (char))
{
	int		i;
	char	*str;

	if (s && f)
	{
		i = 0;
		if ((str = (char*)malloc(sizeof(char) * (ft_strlen(s) + 1))) == 0)
			return (NULL);
		while (s[i])
		{
			str[i] = f(s[i]);
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	else
		return (NULL);
}
